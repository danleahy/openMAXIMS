package ims.core.forms.rieconfirmationdialog;

import ims.framework.enumerations.DialogResult;
import ims.framework.exceptions.PresentationLogicException;

/**
 * @author cperebiceanu
 * This dialog was developed to handle situation where using standard RIE mechanism it's not possible.
 * It copies the standard RIE dialog.
 * So before using it, please make sure that there is no way to implement the standard.
 * Doesn't actually perform the rie, instead uses GC Core.RieMessage to pass the RIE message back to you.
 * I suggest using domain.markAsRie method
 * 
 */
public class Logic extends BaseLogic
{
	private static final long serialVersionUID = 1L;

	@Override
	protected void onBtnCancelClick() throws PresentationLogicException
	{
		engine.close(DialogResult.CANCEL);
		
	}

	@Override
	protected void onBtnOKClick() throws PresentationLogicException
	{
		if (form.txtMessage().getValue() == null )
		{
			engine.showErrors(new String[]{"The reason for marking this record as Recorded In Error is mandatory"});
			return;
		}
		form.getGlobalContext().Core.setRieMessage(form.txtMessage().getValue());
		engine.close(DialogResult.OK);
	}
	
}
