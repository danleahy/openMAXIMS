			//WDEV-17513
			PatientShort ps = form.getGlobalContext().Core.getPatientShort();
			ps.setDod(recordToSave.getAttendance().getPatient().getDod());//WDEV-18074
			form.getGlobalContext().Core.setPatientShort(ps);			
